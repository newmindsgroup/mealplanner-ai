// ============ UTILITY FUNCTIONS ============

export function getFoodsByBloodType(
  bloodType: BloodType,
  classification?: 'beneficial' | 'neutral' | 'avoid'
): FoodItem[] {
  if (classification) {
    return bloodTypeFoodDatabase.filter(
      (food) => food.classification[bloodType] === classification
    );
  }
  return bloodTypeFoodDatabase;
}

export function searchFoods(query: string, bloodType?: BloodType): FoodItem[] {
  const lowerQuery = query.toLowerCase();
  let results = bloodTypeFoodDatabase.filter((food) =>
    food.name.toLowerCase().includes(lowerQuery) ||
    food.category.toLowerCase().includes(lowerQuery) ||
    food.subcategory?.toLowerCase().includes(lowerQuery)
  );

  if (bloodType) {
    results.sort((a, b) => {
      const aClass = a.classification[bloodType];
      const bClass = b.classification[bloodType];
      const order = { beneficial: 0, neutral: 1, avoid: 2 };
      return order[aClass] - order[bClass];
    });
  }

  return results;
}

export function getFoodsByCategory(
  category: string,
  bloodType?: BloodType
): FoodItem[] {
  let results = bloodTypeFoodDatabase.filter((food) => food.category === category);

  if (bloodType) {
    results.sort((a, b) => {
      const aClass = a.classification[bloodType];
      const bClass = b.classification[bloodType];
      const order = { beneficial: 0, neutral: 1, avoid: 2 };
      return order[aClass] - order[bClass];
    });
  }

  return results;
}

export function getFoodById(id: string): FoodItem | undefined {
  return bloodTypeFoodDatabase.find((food) => food.id === id);
}

export function getCategoryStats(bloodType: BloodType) {
  const stats: Record<string, { beneficial: number; neutral: number; avoid: number }> = {};

  bloodTypeFoodDatabase.forEach((food) => {
    if (!stats[food.category]) {
      stats[food.category] = { beneficial: 0, neutral: 0, avoid: 0 };
    }
    const classification = food.classification[bloodType];
    stats[food.category][classification]++;
  });

  return stats;
}

export function getTotalStats(bloodType: BloodType) {
  const stats = { beneficial: 0, neutral: 0, avoid: 0 };

  bloodTypeFoodDatabase.forEach((food) => {
    const classification = food.classification[bloodType];
    stats[classification]++;
  });

  return stats;
}

